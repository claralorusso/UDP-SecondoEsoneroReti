#include <string.h>


//funzione che converte il char passato come parametro nel char equivalente maiuscolo
char convert(char c){
		    switch (c)
		    {
		    case 'a':
		    	return c = 'A';
		    	break;
		    case 'e':
		    	return c = 'E';
		    	break;
		    case 'i':
		    	return c = 'I';
		    	break;
		    case 'o':
		    	return c = 'O';
		    	break;
		    case 'u':
		    	return c = 'U';
		    	break;
		    }

return 0;
}


